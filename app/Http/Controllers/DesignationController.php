    {
        // dd($request);
        $designations = new Designation;
        $designations->designation_title = $request->designation_title;
        $designations->designation_desccription = $request->designation_desccription;
        $designations->save();
        return redirect()->route('designation.index')
                         ->with('success','Designation create Successful');
    }

